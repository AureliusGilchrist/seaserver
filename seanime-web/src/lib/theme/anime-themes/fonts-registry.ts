/**
 * Creates a curated 10-font palette for each theme.
 * Organized from stylistic → plain.
 */
export function getThemeFontPalette(count: number = 10): ThemeFont[] {
    // Distribute across all categories for variety
    const distributions = [
        displayFonts[0],      // Most stylistic
        serifFonts[0],
        roundedSansFonts[0],
        geometricSansFonts[0],
        sansSerifFonts[0],    // Mid-range
        sansSerifFonts[1],
        sansSerifFonts[2],
        monospaceFonts[0],
        sansSerifFonts[3],
        sansSerifFonts[4],    // Most boring/plain
    ]
    return distributions.slice(0, Math.min(count, distributions.length))
}

/**
 * For each theme, returns the exact same 10-font palette.
 * This ensures consistency across all themes while allowing selection.
 */
export const DEFAULT_THEME_FONTS: ThemeFont[] = getThemeFontPalette(10)
