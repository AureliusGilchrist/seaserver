/**
 * Get all search queries for a theme (primary + synonyms).
 * Returns deduplicated list of queries to search.
 */
export function getWallhavenSearchQueries(themeId: AnimeThemeId): string[] {
    const primary = WALLHAVEN_CURATED_QUERY[themeId]
    const synonyms = WALLHAVEN_SYNONYM_QUERIES[themeId] || []

    const queries = new Set<string>()
    if (primary) queries.add(primary)
    synonyms.forEach(q => queries.add(q))

    return Array.from(queries).filter(q => q.length > 0)
}
